#include "Wall.h"
#include "DIBitmap.h"
#include "data_declaration.h"

//////////////////////////////////////////////////////////////////////
// Construction/Destruction
//////////////////////////////////////////////////////////////////////

Wall::Wall(int x, int y)
{
	pos_x = x;
	pos_y = y;
	//id = WALL;
	wallBitmap = new DIBitmap(WALLBITMAP);
}

Wall::~Wall()
{
	delete wallBitmap;
}

//////////////////////////////////////////////////////////////////////
// Show wall on the screen
//////////////////////////////////////////////////////////////////////

void
Wall::display()
{
	wallBitmap->drawPixels(pos_x, pos_y);
}